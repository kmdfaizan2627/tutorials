package com.formula;

public class Swapping {
public static void main(String[] args) {
	int a= 10;
	int b= 20;
	
	System.out.println("Before Swapping Number"+a);
	System.out.println("Before Swapping Number"+b);
	
int c;
c=a;
a=b;
b=c;
System.out.println("Before Swapping Number"+a);
System.out.println("Before Swapping Number"+b);

}
}
